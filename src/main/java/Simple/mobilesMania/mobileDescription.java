package Simple.mobilesMania;

public class mobileDescription {

	int modelNumber;
	String mobileModelName;
	int mobilePrice;
	int mobileRam;
	String userInterface;

	public mobileDescription(int modelNumber, String mobileModelName, int mobilePrice, int mobileRam, String userInterface) {
		this.modelNumber = modelNumber;
		this.mobileModelName = mobileModelName;
		this.mobilePrice = mobilePrice;
		this.mobileRam = mobileRam;
		this.userInterface = userInterface;
	}
	
}
